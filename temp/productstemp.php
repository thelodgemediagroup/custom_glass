		<h1>Anti-Reflective Glass</h1>
		<h3>Anti-Reflective Glass Subhead</h3>
		<p>Guardian’s anti-reflective (AR) glass has a durable, multilayer, sputtered anti-reflective coating applied to our own high quality float glass. The product is economical and highly effective at reducing reflectance and enhancing transmission – a combination that enables designers and engineers to achieve optimal performance. 
			Guardian creates the AR product by vacuum depositing a multilayer anti-reflective coating developed at our Science and Technology Center onto our own high quality float glass. As the characteristics of the glass substrate are critical to overall performance, the glass itself is stringently specified and selected to ensure overall quality meets specifications desired by our customers. Automated in-line optical inspection throughout the float glass manufacturing and coating processes ensures quality, uniformity and consistency of final product. 
			Guardian AR glass is offered with a single sided coating standard and is available with a double sided coating on request. The product is offered on glass substrates up to 96” by 144” and available in high volumes.
