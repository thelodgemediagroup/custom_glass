		<h1>Insulating Glass</h1>
		<h3>Insulating Glass Subhead</h3>
		<p>Insulating Glass (or glazing) is comprised of two panes of glass separated by an air or other gas filled space to reduce heat transfer. Typically used in school buses, Insulating Glass helps keep heat in during the winter and out during the summer. Insulating Glass is also used for its acoustic attenuation properties. The air or gas filled void improves the noise insulation quality and improves acoustical performance by reducing exterior noise transmission. Custom Glass Solutions has provided Insulating Glass as a “mid lite” (Insulating Glass Unit that is between the driver and the engine) for a number of niche, rear engine sports cars.</p>
